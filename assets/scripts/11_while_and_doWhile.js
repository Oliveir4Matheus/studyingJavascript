/*
O while executa tudo o que está dentro dele até que uma condição seja atendida.

Já o do while,primeiro executa tudo o que está dentro dele e depois verifica se a condição foi atendida e se ela não foi ele continua repetindo o bloco de códgo dentro dele.


*/

let i = 0;

while(i <= 15) {
    console.log(i)
    i++
}
i = 14

do {
    console.log(i)
} while(i>14)